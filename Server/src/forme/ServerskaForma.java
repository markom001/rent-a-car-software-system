    private void zaustaviServer() {
        ps.interrupt();
        this.dispose();
        JOptionPane.showMessageDialog(this, "Server je uga≈°en - kraj rada programa");
        System.exit(0);
    }

    public void popuniKombo(ArrayList<Prodavac> listaProdavaca) {
        
        cmbPrijavljeni.removeAllItems();
        for (Prodavac prodavac : listaProdavaca) {
            cmbPrijavljeni.addItem(prodavac);
        }       
    }
