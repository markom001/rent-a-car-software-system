    private void srediFormu() throws Exception {
       
        txtImePrezime.setText(musterija.getImePrezime());
        txtAdresa.setText(musterija.getAdresa());
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        txtDatumRodjenja.setText(sdf.format(musterija.getDatumRodjenja()));
        cmbKategorija.removeAllItems();
        for(KategorijaVozaca kv : KlijentKontroler.getInstance().ucitajListuKategorijaVozaca()){
            cmbKategorija.addItem(kv);
        }
        cmbKategorija.getModel().setSelectedItem(musterija.getKategorijaVozaca());     
    }
