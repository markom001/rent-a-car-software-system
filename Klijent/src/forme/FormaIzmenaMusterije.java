    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        
        
        String imePrezime = txtImePrezime.getText();
        String datum = txtDatumRodjenja.getText();
        String adresa = txtAdresa.getText();
        KategorijaVozaca kv = (KategorijaVozaca) cmbKategorija.getSelectedItem();
        
        if (imePrezime.isEmpty() || datum.isEmpty() || adresa.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja su obavezna!");
            return;
        }
        
        if (!imePrezime.contains(" ")) {
            JOptionPane.showMessageDialog(this, "Moraju se uneti i ime i prezime!");
            return;        
        }
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        Date datumRodjenja;
        
        try {
            datumRodjenja = sdf.parse(datum);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Format datuma nije ispravan!");
            return;
        }
        
         if (datumRodjenja.after(new Date())) {
            JOptionPane.showMessageDialog(this, "Datum rođenja ne može biti posle današnjeg datuma");
            return;
        }
        
        if (!adresa.contains(" ")) {
            JOptionPane.showMessageDialog(this, "Moraju se uneti i naziv ulice i kućni broj");
            return;        
        }
         
        boolean postojiBroj = false;
        
        for(int i = 0; i < adresa.length(); i++){
            if(Character.isDigit(adresa.charAt(i))){
                postojiBroj = true;
            }                      
        }
        
        if(!postojiBroj){
            JOptionPane.showMessageDialog(this, "Potrebno je uneti kućni broj");
            return;
        }
        
        if(!Character.isDigit(adresa.charAt(adresa.length() - 1))){
            JOptionPane.showMessageDialog(this, "Adresa se mora zavrsavati kućnim brojem");
            return;
        }
        
        try {
            musterija.setImePrezime(imePrezime);
            musterija.setDatumRodjenja(datumRodjenja);
            musterija.setAdresa(adresa);
            musterija.setKategorijaVozaca(kv);           
            KlijentKontroler.getInstance().izmeniMusteriju(musterija);
            JOptionPane.showMessageDialog(this, "Sistem je izmenio mušteriju!");
            FormaPretragaMusterija fpm = (FormaPretragaMusterija) getParent();
            fpm.osveziTabelu();
            this.dispose();
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da izmeni mušteriju!");
        }    
        
    }//GEN-LAST:event_btnIzmeniActionPerformed


